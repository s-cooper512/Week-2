let color = "pretty color goes here";

if (color === "green" || color === "Green") {
    console.log("GO");
} else if (color === "yellow" || color === "Yellow") {
    console.log("SLOW");
} else if (color === "red" || color === "Red") {
    console.log("STOP");
} else {
    console.log("CONFUSED");
}